void main() {

  // Cuando se realiza una declaración sin realizar una asignación, se produce un error.
  // Soluciona este error mediante el uso de null safe.

  int? numero;

  print('$numero');

  // Para que no se produzca un error, colocamos un signo de interrogación (?)
  // al final del tipo de variable. Esto indica que la variable puede ser null
  
}